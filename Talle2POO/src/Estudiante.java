public class Estudiante {

    private String nombre;
    private int edad;
    private int numero_estudiante;

    //constructor
    public Estudiante(String nombre, int edad, int numero_estudiante){
        this.nombre = nombre;
        this.edad = edad;
        this.numero_estudiante = numero_estudiante;
    }

    //getter y setter

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {

        return edad;
    }

    public int getNumero_estudiante() {

        return numero_estudiante;
    }



    //metodos

    //public void

}
